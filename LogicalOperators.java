public class LogicalOperators {
public static void main(String[] args) {
boolean bool1 = true;
boolean bool2 = false;
System.out.println("Logical AND (&&): " + (bool1 && bool2));
System.out.println("Logical OR (||): " + (bool1 || bool2));
System.out.println("Logical NOT (!): " + !bool1);
    }
}